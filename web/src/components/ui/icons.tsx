// アイコンは既定で「装飾」扱い:aria-hidden + focusable=false で AT から隠す。
// ただし consumer が名前(aria-label / aria-labelledby)か role を渡したときは
// 「意味のある図」とみなし、aria-hidden を強制せず role="img" を補う。
// 副作用レジストリを持たない純関数なので各 export の tree-shaking を妨げない。
function a11yProps(props: React.SVGProps<SVGSVGElement>): React.SVGProps<SVGSVGElement> {
  const named =
    props["aria-label"] != null || props["aria-labelledby"] != null || props.role != null;
  if (named) {
    // 名前付き図:role 未指定なら img を補う。aria-hidden は強制しない。
    return { role: "img", ...props };
  }
  // 装飾既定:consumer が明示した値があればそれを優先する。
  return { "aria-hidden": true, focusable: false, ...props };
}

